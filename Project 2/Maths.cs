using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Project_2
{
    internal class Maths
    {
        public static decimal Add(decimal Op1, decimal Op2)
        {
            return Op1 + Op2;
        }
        public static decimal Sub(decimal Op1, decimal Op2)
        {
            return Op1 - Op2;
        }
        public static decimal Mul(decimal Op1, decimal Op2)
        {
            return Op1 * Op2;
        }
        public static decimal Div(decimal Op1, decimal Op2)
        {
            return Op1 / Op2;
        }
    }
}
